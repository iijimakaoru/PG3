#include <iostream>
#include <vector>
#include <list>

using namespace std;

int main()
{
	// 山手線
	list<const char*> stationNames
	{
		"Tokyo",
		"Kanda",
		"Akihabara",
		"Okachimachi",
		"Ueno",
		"Uguisudani",
		"Nippori",
		"Tabata",
		"Komagome",
		"Sugamo",
		"Otuka",
		"Ikebukuro",
		"Mejiro",
		"Takadanohara",
		"Shin-Okubo",
		"Shinjuku",
		"Yoyogi",
		"Harajuku",
		"Shibuya",
		"Ebisu",
		"Meguro",
		"Gotanda",
		"Osaki",
		"Shinagawa",
		"Tamachi",
		"Hamamatutsucho",
		"Shimbasi",
		"Yurakucho"
	};

	// 1970
	printf("1970年\n");
	for (auto itr = stationNames.begin(); itr != stationNames.end(); ++itr)
	{
		cout << *itr << "\n";
	}
	printf("\n");

	// 2019
	printf("2019年\n");
	list<const char*>::iterator stationNames1970 = next(stationNames.begin(), 7);
	stationNames.insert(stationNames1970, "Nishi-Nippori");
	for (auto itr = stationNames.begin(); itr != stationNames.end(); ++itr)
	{
		cout << *itr << "\n";
	}
	printf("\n");

	// 2022
	printf("2022年\n");
	list<const char*>::iterator stationNames2019 = next(stationNames.begin(), 25);
	stationNames.insert(stationNames2019, "TakanawaGateWay");
	for (auto itr = stationNames.begin(); itr != stationNames.end(); ++itr)
	{
		cout << *itr << "\n";
	}
	printf("\n");

	return 0;
}